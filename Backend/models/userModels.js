const mongoose = require("mongoose");

const userSchema = mongoose.Schema({
  title: String,
  message: String,
  creator: String,
  tags: [String],
  selectedFile: String,
  likeCount: {
    type: Number,
    default: 0,
  },
  createdAt: {
    type: Date,
    default: new Date(),
  },
});

const PostUser = mongoose.model("PostUser", userSchema);

module.exports = PostUser;
